// Logical And(&&)
console.log(true && true);
console.log(true && false);
console.log(false && true);
console.log(false && false);
console.log(1 && 1);

// Logical OR (||)

console.log(true || true);
console.log(true || false);
console.log(false || true);
console.log(false || false);

// Logical Not (!)

console.log(!false);
console.log(!true);

console.log(4 > 3 && 10 > 5);
console.log(4 < 3 && 10 > 5);
console.log(4 > 3 && 10 < 5);
console.log(4 < 3 && 10 < 5);

console.log(4 > 3 || 10 > 5);
console.log(4 < 3 || 10 > 5);
console.log(4 > 3 || 10 < 5);
console.log(4 < 3 || 10 < 5);