let x = 10;
let y = 4;
x += 3;   // x = x + 3;
console.log(x);
x -= 3;   // x = x - 3;
console.log(x);
x *= 3;   // x = x * 3;
console.log(x);
x /= 3;   // x = x / 3;
console.log(x);
x %= 3;   // x = x % 3;
console.log(x);

x += y;   // x = x + y;
console.log(x);
